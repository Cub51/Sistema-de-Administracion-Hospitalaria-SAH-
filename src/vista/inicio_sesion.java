    public Cuenta buscarCuenta() {
        for (Object c : cuentaDAO.listarCuentas()) {
            if (this.txtClave.getText().equals(seguridad.Desencriptar(cuentaDAO.buscarCuenta((Cuenta) c).getClave()))
                    && this.txtUsuario.getText().equals(cuentaDAO.buscarCuenta((Cuenta) c).getUsuario())) {
                //sw = true;
                return (Cuenta) c;
            }
        }
        return null;
    }

    public void ingresarSistema() {
        Cuenta c = buscarCuenta();
        if (c == null) {
            JOptionPane.showMessageDialog(null, "Los dato/s ingresados son incorrectos", "ERROR: Autenticación", JOptionPane.WARNING_MESSAGE);
            this.txtUsuario.setText("");
            this.txtClave.setText("");
        } else {
            
            switch (c.getPersona().getRol().getNombre_rol()) {
                case "MEDICO":
                    PaginaPrincipalMedico form = new PaginaPrincipalMedico(c.getPersona());
                    form.setVisible(true);
                    this.dispose(); break;
                case "ATENCION":
                    Frm_PrincipalAtencion_Cliente form1 = new Frm_PrincipalAtencion_Cliente(c.getPersona());
                    form1.setVisible(true);
                    this.dispose(); break;
                case "LABORATORISTA":
                    Frm_Principal_Laboratorio form2 = new Frm_Principal_Laboratorio(c.getPersona());
                    form2.setVisible(true);
                    this.dispose(); break;
                case "ADMINISTRADOR":
                    Frm_PrincipalAdministracion form3 = new Frm_PrincipalAdministracion(c.getPersona());
                    form3.setVisible(true);
                    this.dispose(); break;
            }
        }

    }

